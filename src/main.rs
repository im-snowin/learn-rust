fn get_input(prompt: &str, input_type: &str) -> String {
    let mut input = String::new();
    println!("{}: ", prompt);
    io::stdin()
        .read_line(&mut input)
        .expect("Failed to read the input!");

    if input_type == "int" {
        let int_input = input.trim().parse().unwrap();
        return int_input;
    }

    return input;
}
